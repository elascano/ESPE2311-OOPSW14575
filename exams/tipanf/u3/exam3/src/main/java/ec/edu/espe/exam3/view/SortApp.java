    private void txtAddNumberButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtAddNumberButtonMouseClicked
        int number = Integer.parseInt(tdfInsertNumber.getText());
        unsortedData.add(number);
        
        tdfInsertNumber.setText("");
    }//GEN-LAST:event_txtAddNumberButtonMouseClicked

    private void txtAddSortButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtAddSortButtonMouseClicked
        SortingContext sc = new SortingContext();
        sortedData = sc.sort(unsortedData);
        String algorithm = sc.getAlgorithmType();
        
        sendToDB(algorithm);
        clearFields();
    }//GEN-LAST:event_txtAddSortButtonMouseClicked

    private void clearFields(){
        tdfInsertNumber.setText("");
        unsortedData.clear();
        sortedData.clear();
    }
    
    private void sendToDB(String algorithm){
        SortArray sortArray = new SortArray(unsortedData,algorithm,sortedData);
        SortArrayController sac = new SortArrayController();
        sac.create(sortArray);
        
    }
    
    
