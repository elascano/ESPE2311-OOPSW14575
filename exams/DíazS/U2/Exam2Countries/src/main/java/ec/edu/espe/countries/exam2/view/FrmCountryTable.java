    public void aforeTable() {
        DefaultTableModel model = new DefaultTableModel();
        tblCountries.setModel(model);
        model.addColumn("Id");
        model.addColumn("Name");
        model.addColumn("Total Population");
        model.addColumn("Foreign Population");
        model.addColumn("Percentage");

        DecimalFormat df = new DecimalFormat("#.##"); 

        for (Country country : CountryController.show()) {
            float totalPopulation = country.getTotalPopulation();
            float foreignPopulation = country.getForeignPopulation();

            float percentage = (totalPopulation != 0) ? (foreignPopulation / totalPopulation) * 100 : 0;

            Object[] row = {country.getId(), String.valueOf(country.getName()), (int) totalPopulation, (int) foreignPopulation, df.format(percentage)};
            model.addRow(row);
        }
    }
    


