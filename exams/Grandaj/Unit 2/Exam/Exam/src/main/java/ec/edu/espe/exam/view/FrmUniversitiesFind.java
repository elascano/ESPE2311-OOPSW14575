    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

// Obtener el ID ingresado por el usuario
        String idText = jTextField1.getText();
        // Convertir el ID a un tipo de datos int
        int id = Integer.parseInt(idText);

        University university = CountryController.getInstance().obtain(id);
        if (university != null) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Limpiar la tabla antes de agregar nueva información
            model.addRow(new Object[]{university.getId(), university.getName(), university.getYear()});
        } else {
            JOptionPane.showMessageDialog(this, "University not found.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

