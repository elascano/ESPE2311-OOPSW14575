    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String id = txfid.getText();
        String name = txfName.getText();
        double priceSub = Double.parseDouble(txfPrecio.getText());
        
        double priceTotal = CellphoneController.calculatePriceTotal(priceSub);
        int selection;
        selection = JOptionPane.showConfirmDialog(rootPane, "Esta seguro de insertar el telefono ?","Logout",JOptionPane.YES_NO_OPTION);
        
        
        if (selection==JOptionPane.YES_OPTION){
        Cellphone cellphone = new Cellphone(id, name, priceSub, priceTotal);
        if(DataBaseController.saveObject(cellphone)){
            JOptionPane.showMessageDialog(null, "Objeto Ingresado");
            txfName.setText("");
            txfPrecio.setText("");
            txfid.setText("");
        }
        
        }
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

