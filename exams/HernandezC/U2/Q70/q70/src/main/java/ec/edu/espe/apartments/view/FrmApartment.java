    private void btnAddClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddClientActionPerformed
        int id = Integer.parseInt(txtId.getText());
        int size = Integer.parseInt(txtSize.getText());
        int price = Integer.parseInt(txtPriceByMeter.getText());
        String street = txtStreet.getText();
        int totalPrice = size * price;

        // Llama al método addClient de la clase Client y le pasa la lista existente
        Apartment newApartment = new Apartment(id, size, street, price, totalPrice);
        ApartmentController.addToMongo(newApartment);

        String contactStr= "Id: " + newApartment.getId() + "\nStreet:"+ newApartment.getMainStreet() +"\nSize:"+ newApartment.getSizeInMeters() +
                "\nPrice by meters:" + newApartment.getPriceByMeter() + "\nTotal Price:"+ newApartment.getTotalPrice()+"\n is being added";
        
        JOptionPane.showMessageDialog(rootPane, contactStr);

    }//GEN-LAST:event_btnAddClientActionPerformed

