    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRowIndex = tableComputers.getSelectedRow();
        if (selectedRowIndex != -1) {
            String productId = tableComputers.getValueAt(selectedRowIndex, 0).toString();

            deleteProductFromDatabase(productId);

            DefaultTableModel model = (DefaultTableModel) tableComputers.getModel();
            model.removeRow(selectedRowIndex);
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un vehiculo para eliminar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

