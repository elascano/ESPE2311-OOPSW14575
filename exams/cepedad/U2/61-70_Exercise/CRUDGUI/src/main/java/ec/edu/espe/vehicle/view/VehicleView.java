    private void updateTable() {
        // Recuperamos los datos de la base de datos y los mostramos en la tabla
        DefaultTableModel model = (DefaultTableModel) tableComputers.getModel();
        model.setRowCount(0); // Limpiamos la tabla antes de agregar los nuevos datos

        // Recuperamos los datos de la base de datos y los mostramos en la tabla
        try (MongoClient mongoClient = MongoClients.create("mongodb+srv://davidcepeda13:david@cluster0.fvgdrtu.mongodb.net/")) {
            MongoDatabase database = mongoClient.getDatabase("FAR_DB");
            MongoCollection<Document> collection = database.getCollection("Vehicles");

            FindIterable<Document> cursor = collection.find();
            for (Document doc : cursor) {
                String id = doc.getObjectId("_id").toString();
                String brand = doc.getString("attribute1");
                String color = doc.getString("attribute2");

                // Agregamos los datos a la tabla
                model.addRow(new Object[]{id, brand, color});
            }
        } catch (Exception e) {
            System.err.println("Error al recuperar los datos de la base de datos: " + e);
        }
    }
    
