    private void ReadSchools() {
    DefaultTableModel model = (DefaultTableModel) tblShowSchools.getModel();
    model.setRowCount(0);

    MongoCollection<Document> collection = SchoolsController.getFromCollection("Schools", Document.class);

    FindIterable<Document> iterable = collection.find();

    for (Document document : iterable) {
        Integer idInteger = document.getInteger("id");
        int id = (idInteger != null) ? idInteger.intValue() : 0;
        String nameOftheSchool = document.getString("nameOftheSchool");
        String direccion = document.getString("direccion");
        Integer numberOfStudentsInteger = document.getInteger("numberOfStudents");
        int numberOfStudents = (numberOfStudentsInteger != null) ? numberOfStudentsInteger.intValue() : 0;
        model.addRow(new Object[]{id, nameOftheSchool, direccion, numberOfStudents});
    }
}
