    private void btnBackCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackCreateActionPerformed
        int option = JOptionPane.showConfirmDialog(this, "¿Seguro desea regresar?", "Confirmación", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION) {
            FrmProfessors professorsForm = new FrmProfessors();
            professorsForm.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btnBackCreateActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        int id = Integer.parseInt(txtId.getText());
        String name = txtName.getText();
        float salary = Float.parseFloat(txtSalary.getText());

        ProfessorController professorController = ProfessorController.getInstance();
        float annualSalary = professorController.calculateAnnualSalary(salary);

        float tax = professorController.calculateTax(salary);

        Professor newProfessor = new Professor(id, name, salary, annualSalary, tax);

        professorController.save(newProfessor);

        JOptionPane.showMessageDialog(this, "Professor creado exitosamente.", "Creación Exitosa", JOptionPane.INFORMATION_MESSAGE);

        FrmProfessors professorsForm = new FrmProfessors();
        professorsForm.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnCreateActionPerformed

