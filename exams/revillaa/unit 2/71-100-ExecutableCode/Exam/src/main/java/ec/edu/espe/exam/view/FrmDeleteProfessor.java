    private void btnContinueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinueActionPerformed
        String idToDelete = txtIdDelete.getText();
        int professorId = Integer.parseInt(idToDelete);

        ProfessorController professorController = ProfessorController.getInstance();

        if (professorController.existsProfessor(professorId)) {
            int option = JOptionPane.showConfirmDialog(this, "¿Está seguro de borrar este Professor?", "Confirmación", JOptionPane.YES_NO_OPTION);

            if (option == JOptionPane.YES_OPTION) {
                professorController.delete(professorId);

                JOptionPane.showMessageDialog(this, "Professor eliminado exitosamente.", "Eliminación Exitosa", JOptionPane.INFORMATION_MESSAGE);

                FrmProfessors professorsForm = new FrmProfessors();
                professorsForm.setVisible(true);
                dispose();
            }
            // Si el usuario elige "No", no haces nada
        } else {
            // Si el profesor no existe, mostrar un mensaje de error
            JOptionPane.showMessageDialog(this, "No existe un Professor con ese ID.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnContinueActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        int option = JOptionPane.showConfirmDialog(this, "¿Seguro desea regresar?", "Confirmación", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION) {
            // Regresar a FrmProfessors
            FrmProfessors professorsForm = new FrmProfessors();
            professorsForm.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btnBackActionPerformed

