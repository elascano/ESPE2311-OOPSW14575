    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUserName.getText().trim();
        char[] passwordChars = txtPassword.getPassword();
        String password = String.valueOf(passwordChars).trim();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Algun campo está vacío, inténtelo de nuevo", "ERROR DE INGRESO", JOptionPane.INFORMATION_MESSAGE);
        } else {
            loginController.migratePasswordsToBCrypt();

            if (loginController.checkCredentials(username, password)) {
                int confirmResult = JOptionPane.showConfirmDialog(this, "Inicio de sesión exitoso", "Inicio de sesión", JOptionPane.OK_CANCEL_OPTION);

                if (confirmResult == JOptionPane.OK_OPTION) {
                    if (professorsForm == null) {
                        professorsForm = new FrmProfessors();
                    }

                    professorsForm.setVisible(true);
                    dispose();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Credenciales inválidas", "Inicio de sesión fallido", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

