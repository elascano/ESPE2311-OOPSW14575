    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
         String idToSearch = jTextField1.getText();  // Obtén el ID de la interfaz

    // Realiza la búsqueda en la base de datos
    MongoCollection<Document> collection = MongoConnection.connect();
    Document query = new Document("id", idToSearch);
    FindIterable<Document> cursor = collection.find(query);

    // Procesa los resultados de la búsqueda
    if (cursor.iterator().hasNext()) {
        // Se encontró un documento con el ID proporcionado
        Document foundDocument = cursor.first();

        // Formatea la información para mostrarla en el JOptionPane
        StringBuilder message = new StringBuilder("Find Document:\n");
        for (String key : foundDocument.keySet()) {
            message.append(key).append(": ").append(foundDocument.get(key)).append("\n");
        }

        // Muestra un mensaje de éxito
        JOptionPane.showMessageDialog(this, message.toString(), "Successful search", JOptionPane.INFORMATION_MESSAGE);
    } else {
        // No se encontró ningún documento con el ID proporcionado
        // Muestra un mensaje de advertencia
        JOptionPane.showMessageDialog(this, "No document was found with that id", "Warning", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btSearchActionPerformed

