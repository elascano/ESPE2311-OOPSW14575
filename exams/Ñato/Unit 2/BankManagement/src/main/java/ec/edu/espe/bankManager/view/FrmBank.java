    void loadBank(MongoClient mongoClient) {
        MongoCollection<Document> collection = DBManager.getCollection(mongoClient);
        model.setRowCount(0);
        
        List<Document> allDocuments = collection.find().into(new ArrayList<>());
        
        for (Document document : allDocuments) {
            int id = document.getInteger("id");
            String name = document.getString("name");
            int loan = document.getInteger("loan");
            int iva = document.getInteger("iva");
            
            model.addRow(new Object[]{id, name, loan, iva});
        }
    }

    void createMeeting(MongoClient mongoClient) {
        MongoCollection<Document> collection = DBManager.getCollection(mongoClient);
 
        Bank newLoan = new Bank(23, "Carlos", 3000, 12);
        
        collection.insertOne(newLoan.toDocument());
        
        model.addRow(new Object[]{
            newLoan.getId(),
            newLoan.getName(),
            newLoan.getLoan(),
            newLoan.getIva()
        });
        
    }

