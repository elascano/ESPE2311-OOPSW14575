    public void aforeTable() {
        SeeAllHandler seeAllHandler = new SeeAllHandler();
        DefaultTableModel model = new DefaultTableModel();
        tblItems.setModel(model);
        model.addColumn("Id");
        model.addColumn("Name");
        model.addColumn("Type");
        model.addColumn("Years");
        
        
        for (SoccerPlayer item : seeAllHandler.getAllItem()) {
                Object[] row = {item.getId(), item.getName(), item.getType(), item.getYears()};
                model.addRow(row);
        }
    }

