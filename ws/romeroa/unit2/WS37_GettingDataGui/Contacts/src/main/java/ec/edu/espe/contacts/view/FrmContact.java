    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        contact = createContactFromForm();
        JOptionPane.showMessageDialog(rootPane, contact);
        clearFields();
    }//GEN-LAST:event_btnRegisterActionPerformed

    private Contact createContactFromForm() throws NumberFormatException {
        id = Integer.parseInt(txtId.getText());
        lastName = txt_lastname.getText();
        firstName = txtFirstName.getText();
        cellPhoneNumber = txtCellphoneNumber.getText();
        contactThatIntroduce = new Contact();
        typeOfContact = cmbType.getSelectedItem().toString();
        birthDate = new Date(ftfBirthDate.getText());
        hobbies = new ArrayList();
        hobbies.add(listHobbies.getSelectedValue());
        gender = 'M';
        comments = txtaComments.getText();
        yearsOfFriendship = sldYearsOfFriendship.getValue();
        
        return new Contact(id, lastName, firstName, cellPhoneNumber, contactThatIntroduce, typeOfContact, birthDate, hobbies, gender, comments, yearsOfFriendship);
    }
    
    private void clearFields() {
        txtId.setText("");
        txt_lastname.setText("");
        txtFirstName.setText("");
        txtCellphoneNumber.setText("");
        cmbType.setSelectedIndex(0);
        ftfBirthDate.setText("");
        listHobbies.setSelectedIndex(0);
        txtaComments.setText("");
        sldYearsOfFriendship.setValue(0);
    }

