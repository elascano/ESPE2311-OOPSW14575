    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed
        double newTaxPercentage = Double.parseDouble(txtNewTaxPorcentage.getText());
        EcTax.getInstance().setTaxPercentage(newTaxPercentage);

        // Obtén la instancia actual de FrmTaxCalculator
        FrmTaxCalculator taxCalculator = findFrmTaxCalculatorAncestor(this);

        if (taxCalculator != null) {
            // Utiliza el nuevo método para actualizar el campo txtTaxPorcentage
            taxCalculator.updateTaxPercentage(newTaxPercentage);
            // Cierra el formulario actual
            this.dispose();
        } else {
            System.out.println("FrmTaxCalculator instance not found!");
        }
    }//GEN-LAST:event_btnChangeActionPerformed

    private FrmTaxCalculator findFrmTaxCalculatorAncestor(Component component) {
        while (component != null) {
            if (component instanceof FrmTaxCalculator) {
                return (FrmTaxCalculator) component;
            }
            component = component.getParent();
        }
        return null;
    }
    
