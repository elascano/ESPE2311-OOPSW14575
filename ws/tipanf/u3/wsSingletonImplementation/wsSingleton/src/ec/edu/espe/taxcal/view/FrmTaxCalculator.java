    public void updateTaxPercentage(float newPercentage){
        usTax.updateTaxPercentage(newPercentage);
        
        txtTaxPercentage.setText(usTax.getTaxPercentage() + "%");
        
    }
    
    private void updateFinalValue(float finalValue){
        DecimalFormat dfrmt = new DecimalFormat();
	dfrmt.setMaximumFractionDigits(2);
        String updatedValue = dfrmt.format(finalValue);
        
        txtDisplayFinalValue.setText("$ " + updatedValue);
    }
    
