    private void txtTotalValueMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtTotalValueMouseClicked
        String updatedString = fdtInitialPrice.getText().replace(',', '.');
        
        float initialValue = Float.parseFloat(updatedString);
        
        float finalValue = usTax.salesTotal(initialValue);
        
        updateFinalValue(finalValue);
    }//GEN-LAST:event_txtTotalValueMouseClicked

