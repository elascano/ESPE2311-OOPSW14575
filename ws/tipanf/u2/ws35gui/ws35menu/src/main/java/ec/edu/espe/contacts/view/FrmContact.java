    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        readData();
        //System.out.println("Contact Inserted: " + contact);
        
        
        String contactString = contact.getId() + "\n" + contact.getLastName() + "\n" + contact.getFirstName() + "\n" + contact.getCellPhoneNumber() + "\n" + contact.getContactThatIntroduce() + "\n" + contact.getTypeOfContact() + "\n" + contact.getBirthDate() + "\n" +contact.getHobbies() + "\n" + contact.getGender() + "\n" + contact.getComments() + "\n" + contact.getYearsOfFriendship() + "\n"; 
        JOptionPane.showMessageDialog(rootPane,contactString);
        
        ContactController.register(contact);
        emptyData();
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void readData() throws NumberFormatException {
        id = Integer.parseInt(txtId.getText());
        lastName = txtLastName.getText();
        firstName = txtFirstName.getText();
        cellPhoneNumber = txtCellphoneNumber.getText();
        contactThatIntroduce = cmbContact.getSelectedItem().toString();
        typeOfContact = cmbType.getSelectedItem().toString();
        birthDate = new Date();
        hobbies.add(ltsHobbies.getSelectedValue());
        if(rbtnFemale.isSelected()){
            gender = 'F';
        }
        if(rbtnMale.isSelected()){
            gender = 'M';
        }
        
        comments = txtarComments.getText();
        yearsOfFriendship = sldYearsOfFriendship.getValue();
        
        contact = new Contact(id, lastName, firstName, cellPhoneNumber, contactThatIntroduce, typeOfContact, birthDate, hobbies, gender, comments, yearsOfFriendship);
    }
    
    private void emptyData(){
        txtId.setText("");
        txtLastName.setText("");
        txtFirstName.setText("");
        txtCellphoneNumber.setText("");
        cmbContact.setSelectedIndex(0);
        cmbType.setSelectedIndex(0);
        ftfBirthdate.setText("");
        ltsHobbies.setSelectedIndex(0);
        rbtnFemale.setSelected(false);
        rbtnMale.setSelected(false);
        txtarComments.setText("");
        sldYearsOfFriendship.setValue(25);
    }

