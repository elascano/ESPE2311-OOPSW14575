    private void readData() throws NumberFormatException {
        id =Integer.parseInt( txtid.getText());
        lastName=txtLastname.getText();
        firstName=txtFirstname.getText();
        cellPhonenumber=txtCellphonenumber.getText();
        contactThatIntroduce= cmbContact.getSelectedItem().toString();
        type =cmbType.getSelectedItem().toString();
        birthDate = new Date();//read for input
        hobbies.add(lsHobbies.getSelectedValue());//TODO insert all hobbies one by one
        gender = 'H';
        comments = txtaComments.getText();
        yearOfFriendship = sldYeatsOfFriendship.getValue();
        contact = new Contact(id, lastName, firstName, cellPhonenumber, contact, type, birthDate, hobbies, gender, comments, yearOfFriendship);
    }

