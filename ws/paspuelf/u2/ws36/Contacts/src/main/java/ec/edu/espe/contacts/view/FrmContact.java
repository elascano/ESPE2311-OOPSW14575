    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed

        readData();
        System.out.println("contact -> " + contact);
        String contactString = contact.getId() + " \n" + contact.getFirstName() + "\n"+ contact.getLastName() + "\n is being added";
        JOptionPane.showMessageDialog(rootPane, contactString);
        emptyFields();
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void readData() throws NumberFormatException {
        id = Integer.parseInt(txtId.getText());
        lastName = txtLastName.getText();
        firstName = txtFirstName.getText();
        cellPhoneNumber = txtCellphoneNumber.getText();
        contactThatIntroduce = new Contact(1, "Doe", "John", "123-456-7890", null, "Friend", new Date(), new ArrayList<>(List.of("Reading", "Traveling")), 'M', "A great person", 3);
        typeOfContact = cmbType.getSelectedItem().toString();
        birthDate = new Date();
        hobbies.add(lstHobbies.getSelectedValue());
        gender = 'M';
        comments = txaComments.getText();
        yearsOfFriendship = sldYearsOfFriendship.getValue();
        // ctrl + spacebar;
        contact = new Contact(id, lastName, firstName, cellPhoneNumber, contactThatIntroduce, typeOfContact, birthDate, hobbies, gender, comments, yearsOfFriendship);
    }
    
