    private void BregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BregisterActionPerformed
        String idString = txtID.getText();
        idString = idString.replace("ID: ", "");

        try {
            int id = Integer.parseInt(idString);
            System.out.println("ID procesado con Ã©xito: " + id);

            lastName = txtLastName.getText();
            firstName = txtFirstName.getText();
            cellPhoneNumber = txtCellphoneNumber.getText();
            contactThatIntroduce = new Contact();
            type = cmvType.getSelectedItem().toString();
            birthDate = new Date();
            hobbies = new ArrayList<>();
            hobbies.addAll(jList1.getSelectedValuesList());

            gender = 'M';
            comments = jTxtComments.getText();
            yearsOfFriendship = SliderYearsOfFriendship.getValue();
            Document document = new Document("id", id)
                    .append("lastName", lastName)
                    .append("firstName", firstName)
                    .append("cellPhoneNumber", cellPhoneNumber)
                    .append("type", type)
                    .append("birthDate", birthDate)
                    .append("hobbies", hobbies)
                    .append("gender", gender)
                    .append("comments", comments)
                    .append("yearsOfFriendship", yearsOfFriendship);
            MongoDBManager.create(document, "Contacts");

        } catch (NumberFormatException e) {
            System.out.println("Error al procesar ID: " + id);
            e.printStackTrace();
        }
    }//GEN-LAST:event_BregisterActionPerformed

